package br.com.zup.listaAtributos;

public class Bola {
    int circuferencia;
    String cor;
    String marca;
    double velocidade;

    public Bola(){

    }
    public Bola(int circuferencia, String cor, String marca, double velocidade){
        this.circuferencia = circuferencia;
        this.cor = cor;
        this.marca = marca;
        this.velocidade = velocidade;
    }
}
